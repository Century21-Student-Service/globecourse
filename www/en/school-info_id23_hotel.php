<h5>在赴澳留学学习酒店和会展管理的同时，入住酒店带薪实习</h5>
<h6>【内容简介】</h6>

<p>酒店管理和会展管理双高级文凭，寄宿酒店，享受半年免费酒店住宿，一年半带薪实习，学成时可直升大学三年级，高级文凭+本科+硕士共仅四年</p>
<p><img src="./../uploads/image/20151016/12.jpg" width="650" /></p>
<p>
	酒店管理专业，是全球十大热门行业之一，高级酒店管理人才在全球都是一直很紧缺。在国际人才市场上，酒店管理人才更是出现了供不应求的局面。由此，澳洲TAFE西悉尼学院（TAFE Western Sydney）和陡崖集团（Escarpment Group of Hotels）合作推出了酒店管理项目（Hotel Management Hotel）。TAFE西悉尼学院在为学生提供酒店管理知识课程的同时，也通过与陡崖酒店集团的合作使得本专业学生可以得到进入该集团旗下的四大酒店入住并带薪实习的绝佳机会。陡崖集团（Escarpment Group of Hotels）在国内和国际酒店业被公认为质量和服务的代表。酒店管理学院（HMI）的学生将在此接受最优秀的酒店服务业培训和教导，并经由TAFE西悉尼（TAFE Western Sydney）—澳大利亚最大的职业教育机构之一—进行更深入的培训，为众多想学习酒店管理专业的学生提供了赴澳学习酒店管理专业课程并同时入住酒店带薪实习的机会。
</p>

<h6>【课程介绍】</h6>
<p>此课程共两年四个学期，在完成此课程后可获得酒店和会展管理的双高级文凭。</p>
<p>
	获得此双高级文凭后，继续在西悉尼大学入读一年课程，或南十字星大学修读一年半课程，即可获得商科或金融专业的学士学位。获得商科或金融专业的学士学位，继续入读西悉尼大学一年课程即可获得金融管理类硕士学位——酒店和旅游专业同时，学生也可在此两年半课程之前，另外报读一年的厨师四级证书课程，再配套以上两年的酒店和会展管理双高级文凭课程。以便在学成时，学生可以以厨师的职业申请澳洲技术移民。
</p>

